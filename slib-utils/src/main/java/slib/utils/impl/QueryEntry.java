package slib.utils.impl;

import java.util.Map;

/**
 *
 * @author Sébastien Harispe <sebastien.harispe@gmail.com>
 */
public final class QueryEntry implements Map.Entry<String, String> {

    private String key;
    private String value;

    /**
     *
     */
    public QueryEntry() {
    }

    /**
     *
     * @param key
     * @param value
     */
    public QueryEntry(String key, String value) {
        this.key = key;
        this.value = value;
    }

    @Override
    public String getKey() {
        return key;
    }

    @Override
    public String getValue() {
        return value;
    }

    @Override
    public String setValue(String value) {
        String old = this.value;
        this.value = value;
        return old;
    }

    /**
     * Only check if K and V are defined anf if their are not equals to empty
     * string
     *
     * @return boolean true if valid
     */
    public boolean isValid() {
        boolean valid = true;

        if (key == null || key.trim().isEmpty()) {
            valid = false;
        } else if (value == null || value.trim().isEmpty()) {
            valid = false;
        }

        return valid;
    }

    @Override
    public String toString() {
        return "key=" + key + "\tvalue=" + value;
    }

    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + ((key == null) ? 0 : key.hashCode());
        result = prime * result + ((value == null) ? 0 : value.hashCode());
        return result;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        QueryEntry other = (QueryEntry) obj;
        if (key == null) {
            if (other.key != null) {
                return false;
            }
        } else if (!key.equals(other.key)) {
            return false;
        }
        if (value == null) {
            if (other.value != null) {
                return false;
            }
        } else if (!value.equals(other.value)) {
            return false;
        }
        return true;
    }
}